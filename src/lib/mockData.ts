// Function to generate mock scored listings based on buy box criteria
export function generateMockScoredListings(buyBoxCriteria?: BuyBoxCriteria): ScoredListing[] {
  // PROTOTYPE MODE: Just return a good variety of properties with basic scoring
  // Skip complex filtering for now to ensure we always show results
  let filteredListings = [...mockListings];
  
  // For prototype: just take a variety of properties across different price ranges
  // This ensures we always have results to show
  filteredListings = filteredListings.slice(0, 20); // Take first 20 properties
  
  return filteredListings.map((listing): ScoredListing => {
    // PROTOTYPE MODE: Simple scoring that always gives reasonable results
    let score = 60; // Start with a decent base score
    const reasons: string[] = ['Prototype result - basic matching'];
    const dealBreakers: string[] = [];
    
    // Simple scoring based on basic property characteristics
    if (listing.price < 300000) {
      score += 15;
      reasons.push('Affordable price point');
    } else if (listing.price > 800000) {
      score -= 10;
      reasons.push('Higher price range');
    }

    if (listing.yearBuilt > 2000) {
      score += 10;
      reasons.push('Recently built');
    }

    if (listing.schoolRating && listing.schoolRating >= 8) {
      score += 10;
      reasons.push('Good schools');
    }

    if (listing.beds >= 3) {
      score += 5;
      reasons.push('Family-friendly size');
    }

    // Add some variation based on listing ID for variety
    const variation = (listing.id.charCodeAt(listing.id.length - 1) % 30) - 15;
    score += variation;
    
    score = Math.max(40, Math.min(95, score)); // Keep scores in reasonable range

    // Determine badge
    let badge: ScoredListing['badge'];
    let badgeColor: ScoredListing['badgeColor'];
    
    if (score >= 85) {
      badge = 'Great Deal';
      badgeColor = 'green';
    } else if (score >= 70) {
      badge = 'Good Deal';
      badgeColor = 'blue';
    } else if (score >= 55) {
      badge = 'Fair';
      badgeColor = 'gray';
    } else {
      badge = 'Over-Priced';
      badgeColor = 'red';
    }

    return {
      ...listing,
      score,
      badge,
      badgeColor,
      matchReasons: reasons,
      dealBreakers,
      matchDetails: {
        priceScore: Math.floor(score * 0.3),
        locationScore: Math.floor(score * 0.2),
        propertyScore: Math.floor(score * 0.3),
        financialScore: Math.floor(score * 0.2),
        killSwitchTriggered: dealBreakers.length > 0,
      },
    };
  });
}

